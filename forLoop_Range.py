# range(start, stop, step) here, stop = n-1
# default value of start = 0 & step = 1

for item in range(1, 6, 1):
    print(item)

print("<--->")

# Print all even numbers from 1 to 20
for even in range(2, 21, 2):
    print(even)

print("<--->")

# Print squares of first 10 natural numbers
for sq in range(1, 11, 1):
    print("Square of ", sq,"is", sq**2)