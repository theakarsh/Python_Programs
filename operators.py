# Arithmetic Operators
a = 10
b = 5
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a//b)
print(a%b)
print(a**2)

# Comparison Operators
print(a==b)
print(a>=b)
print(a!=b)

# Logical Operators
print(a<b and a>b)
print(a<b or a>b)
print(not(a==b))

# Assignment Operators
a += 1
print(a)
a -= 2
print(a)
a *= 2
print(a)
a /= 2
print(a)